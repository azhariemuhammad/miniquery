class SweetSelector {

  static select (params){
    if (params[0] === '#') {
       return document.getElementById(params.slice(1))
    } else if (params[0] === '.') {
      return document.getElementsByClassName(params.slice(1))
    } else if (params[0] !== '.' && params[0] !== '#') {
      return  document.getElementsByTagName(params)
    }
  }

}






